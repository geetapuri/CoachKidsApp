package com.example.demo;

public class FeeMgmt {
	private String dateOfAttendance;
	private String present;
	private String feePaid;
	private String kidID;
	private String feeID;
	private String feeMgmtID;
	
	
	
	public String getDateOfAttendance() {
		return dateOfAttendance;
	}
	public void setDateOfAttendance(String dateOfAttendance) {
		this.dateOfAttendance = dateOfAttendance;
	}
	public String getPresent() {
		return present;
	}
	public void setPresent(String present) {
		this.present = present;
	}
	public String getFeePaid() {
		return feePaid;
	}
	public void setFeePaid(String feePaid) {
		this.feePaid = feePaid;
	}
	public String getKidID() {
		return kidID;
	}
	public void setKidID(String kidID) {
		this.kidID = kidID;
	}
	public String getFeeID() {
		return feeID;
	}
	public void setFeeID(String feeID) {
		this.feeID = feeID;
	}
	public String getFeeMgmtID() {
		return feeMgmtID;
	}
	public void setFeeMgmtID(String feeMgmtID) {
		this.feeMgmtID = feeMgmtID;
	}
	
	

}
